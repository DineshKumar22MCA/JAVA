package OPERATOR;

public class increment_decrement_op {

    public static void main(String args[])
    {
        //Unary Operators in Java ++ --
        int a=10;
        System.out.println(a);
        //a++; //a=a+1
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
 
 
    }
}
